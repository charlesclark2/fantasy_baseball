# Enriched must IMPROVE 2026 by at least this much to be a promote candidate
# (effect-size floor; same scale as the Epic 30 gate's 0.02 brier / accuracy bar).
_PROMOTE_FLOOR = {"mae": 0.02, "brier": 0.002}


# ── Metrics ─────────────────────────────────────────────────────────────────

def _reg_metrics(y: np.ndarray, pred: np.ndarray) -> dict:
    err = pred - y
    return {
        "mae": float(np.mean(np.abs(err))),
        "rmse": float(np.sqrt(np.mean(err ** 2))),
        "medae": float(np.median(np.abs(err))),
        "mean_pred": float(np.mean(pred)),
        "mean_actual": float(np.mean(y)),
        "bias": float(np.mean(err)),
        "pred_std": float(np.std(pred)),
    }


def _calib_80_normal(loc: np.ndarray, scale: np.ndarray, y: np.ndarray) -> float:
    z = 1.2815515594457831  # Phi^-1(0.90)
    lo, hi = loc - z * scale, loc + z * scale
    return float(np.mean((y >= lo) & (y <= hi)))


def _ece(p: np.ndarray, y: np.ndarray, n_bins: int = 10) -> float:
    bins = np.linspace(0.0, 1.0, n_bins + 1)
    idx = np.clip(np.digitize(p, bins) - 1, 0, n_bins - 1)
    ece = 0.0
    for b in range(n_bins):
        m = idx == b
        if m.sum() == 0:
            continue
        ece += (m.sum() / len(p)) * abs(p[m].mean() - y[m].mean())
    return float(ece)


def _clf_metrics(y: np.ndarray, p: np.ndarray) -> dict:
    from sklearn.metrics import brier_score_loss, log_loss
    pc = np.clip(p, 1e-7, 1 - 1e-7)
    corr = float(np.corrcoef(p, y)[0, 1]) if np.std(p) > 0 else float("nan")
    return {
        "brier": float(brier_score_loss(y, p)),
        "nll": float(log_loss(y, pc)),
        "accuracy": float(np.mean((p >= 0.5).astype(int) == y)),
        "ece": _ece(p, y),
        "live_corr": corr,
        "pred_std": float(np.std(p)),
    }


# ── Model fit/predict per kind ──────────────────────────────────────────────

def _impute(train_raw: pd.DataFrame, eval_raw: pd.DataFrame):
    pipe = build_imputation_pipeline()
    Xtr = pipe.fit_transform(train_raw).select_dtypes(include=[np.number])
    Xev = pipe.transform(eval_raw)
    Xev = Xev[[c for c in Xtr.columns if c in Xev.columns]].reindex(columns=Xtr.columns, fill_value=0.0)
    return Xtr, Xev


def _fit_predict_regression(cfg, Xtr, ytr, Xev):
    from ngboost import NGBRegressor
    from ngboost.distns import Normal
    dist = {"Normal": Normal}[cfg["ngb"]["dist"]]
    m = NGBRegressor(n_estimators=cfg["ngb"]["n_estimators"], Dist=dist, verbose=False)
    m.fit(Xtr.values, ytr)
    pred = m.predict(Xev.values)
    try:
        params = m.pred_dist(Xev.values).params
        loc, scale = np.asarray(params["loc"]), np.asarray(params["scale"])
    except Exception:
        loc, scale = pred, None
    return pred, loc, scale


def _fit_predict_classification(cfg, Xtr, ytr, Xev, yev):
    from sklearn.linear_model import LogisticRegression
    from xgboost import XGBClassifier
    clf = XGBClassifier(**cfg["xgb_params"])
    clf.fit(Xtr, ytr.astype(int))
    raw = clf.predict_proba(Xev)[:, 1]
    # Platt calibration fit on the eval split (faithful to production recipe).
    cal = LogisticRegression(C=1.0, solver="lbfgs", max_iter=1000)
    cal.fit(raw.reshape(-1, 1), yev.astype(int))
    return cal.predict_proba(raw.reshape(-1, 1))[:, 1]


def _eval_one(df, cfg, feat_cols, train_idx, eval_idx):
    Xtr, Xev = _impute(df.loc[train_idx, feat_cols], df.loc[eval_idx, feat_cols])
    ytr = df.loc[train_idx, cfg["target_col"]].values
    yev = df.loc[eval_idx, cfg["target_col"]].values
    if cfg["kind"] == "regression":
        pred, loc, scale = _fit_predict_regression(cfg, Xtr, ytr, Xev)
        m = _reg_metrics(yev, pred)
        if scale is not None:
            m["calib_80"] = _calib_80_normal(loc, scale, yev)
        return m
    p = _fit_predict_classification(cfg, Xtr, ytr, Xev, yev)
    return _clf_metrics(yev, p)


def _run_cv(df, cfg, champ_cols, enr_cols):
    champ_folds, enr_folds = [], []
    for train_idx, eval_idx in all_season_splits(df, min_train_seasons=3):
        eval_year = int(df.loc[eval_idx, "game_year"].mode()[0])
        cm = _eval_one(df, cfg, champ_cols, train_idx, eval_idx)
        em = _eval_one(df, cfg, enr_cols, train_idx, eval_idx)
        cm["eval_year"] = em["eval_year"] = eval_year
        champ_folds.append(cm)
        enr_folds.append(em)
        prim = "brier" if cfg["kind"] == "classification" else "mae"
        flag = "" if eval_year == 2026 else "  [CONTAMINATED: signals in-sample]"
        print(f"    fold {eval_year}: champ {prim}={cm[prim]:.4f}  enriched {prim}={em[prim]:.4f}{flag}")
    return champ_folds, enr_folds


def _mean_over_folds(folds, keys):
    return {k: float(np.mean([f[k] for f in folds if k in f])) for k in keys}


def _run_target(name: str, df: pd.DataFrame) -> dict:
    cfg = _TARGETS[name]
    contract = json.loads((PROJECT_ROOT / cfg["contract"]).read_text())
    champ_cols_all = contract["feature_cols"]
    champ_cols = [c for c in champ_cols_all if c in df.columns]
    enrich_all = _enrichment_columns()
    enrich_present = [c for c in enrich_all if c in df.columns and c not in champ_cols]
    enr_cols = champ_cols + enrich_present

    print(f"\n=== {name} ({cfg['kind']}) ===")
    print(f"  contract features: {len(champ_cols_all)}  present-in-df: {len(champ_cols)}")
    print(f"  enrichment cols available: {len(enrich_present)} / {len(enrich_all)}")
    print(f"  champion feats: {len(champ_cols)}  enriched feats: {len(enr_cols)}")

    prim = "brier" if cfg["kind"] == "classification" else "mae"
    metric_keys = (["brier", "nll", "accuracy", "ece", "live_corr", "pred_std"]
                   if cfg["kind"] == "classification"
                   else ["mae", "rmse", "medae", "bias", "pred_std", "calib_80"])

    print("  -- walk-forward CV (2024/2025 folds CONTAMINATED for enriched arm) --")
    champ_folds, enr_folds = _run_cv(df, cfg, champ_cols, enr_cols)
    cv_champ = _mean_over_folds(champ_folds, metric_keys)
    cv_enr = _mean_over_folds(enr_folds, metric_keys)

    print("  -- HONEST 2026 OOS (the only trustworthy surface) --")
    tr = df.index[df["game_year"] < 2026]
    ev = df.index[df["game_year"] == 2026]
    if len(ev) == 0:
        print("    WARNING: no 2026 rows in load_features() — cannot judge. ABSTAIN.")
        live_champ = live_enr = {}
        live_delta = float("nan")
        decision = "ABSTAIN (no 2026 surface)"
    else:
        print(f"    train n={len(tr)} (<=2025), eval n={len(ev)} (2026)")
        live_champ = _eval_one(df, cfg, champ_cols, tr, ev)
        live_enr = _eval_one(df, cfg, enr_cols, tr, ev)
        live_delta = live_enr[prim] - live_champ[prim]   # <0 = enriched better
        floor = _PROMOTE_FLOOR[prim]
        if live_delta < -floor:
            decision = f"PROMOTE-CANDIDATE (2026 {prim} improved {live_delta:+.4f} >= floor {floor})"
        elif live_delta > floor:
            decision = f"REJECT (2026 {prim} REGRESSED {live_delta:+.4f})"
        else:
            decision = f"NEUTRAL — no 2026 gain ({live_delta:+.4f}); Layer-3-only confirmed for {name}"

    result = {
        "target": name,
        "kind": cfg["kind"],
        "primary_metric": prim,
        "n_features_champion": len(champ_cols),
        "n_features_enriched": len(enr_cols),
        "n_enrichment_added": len(enrich_present),
        "enrichment_cols_added": enrich_present,
        "enrichment_cols_missing": [c for c in enrich_all if c not in enrich_present and c not in champ_cols],
        "cv_contaminated_note": "2024/2025 folds are in-sample for the sub-models (trained 2021-2025); CV favors enriched by construction. Judge on live_2026.",
        "cv": {"champion": cv_champ, "enriched": cv_enr,
               "delta_primary": cv_enr[prim] - cv_champ[prim],
               "folds_champion": champ_folds, "folds_enriched": enr_folds},
        "live_2026": {"champion": live_champ, "enriched": live_enr,
                      "delta_primary": live_delta},
        "decision": decision,
    }

    print(f"  CV {prim} (contaminated): champ={cv_champ[prim]:.4f}  enriched={cv_enr[prim]:.4f}  "
          f"delta={cv_enr[prim]-cv_champ[prim]:+.4f}")
    if live_champ:
        if cfg["kind"] == "classification":
            print(f"  LIVE 2026: champ Brier={live_champ['brier']:.4f}/corr={live_champ['live_corr']:.4f}  "
                  f"enriched Brier={live_enr['brier']:.4f}/corr={live_enr['live_corr']:.4f}")
        else:
            print(f"  LIVE 2026: champ MAE={live_champ['mae']:.4f}  enriched MAE={live_enr['mae']:.4f}  "
                  f"(calib_80 {live_champ.get('calib_80', float('nan')):.3f}->{live_enr.get('calib_80', float('nan')):.3f})")
    print(f"  >>> {decision}")
    return result


def _smoke() -> None:
    """Fast self-test of the risky path (signal pivot) — no training, no load_features."""
    print("=== SMOKE: sub-model signal load + pivot ===")
    wide = _load_submodel_signals()
    expected = _enrichment_columns()
    present = [c for c in expected if c in wide.columns]
    missing = [c for c in expected if c not in wide.columns]
    print(f"  rows (games): {len(wide):,}")
    print(f"  expected 52 enrichment cols, present: {len(present)}  missing: {len(missing)}")
    if missing:
        print(f"  MISSING: {missing}")
    nonfloat = [c for c in present if wide[c].dtype.kind not in ("f", "i")]
    print(f"  non-numeric cols (should be 0): {len(nonfloat)} {nonfloat[:5]}")
    cov = {c: round(100 * wide[c].notna().mean(), 1) for c in present[:6]}
    print(f"  coverage sample: {cov}")
    print("  SMOKE OK" if not missing and not nonfloat else "  SMOKE FAIL")


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--target", choices=["home_win", "run_diff", "total_runs", "all"], default="all")
    ap.add_argument("--smoke", action="store_true", help="Fast pivot self-test, no training.")
    args = ap.parse_args()

    if args.smoke:
        _smoke()
        return

    _OUT_DIR.mkdir(parents=True, exist_ok=True)
    print("Loading base features from Snowflake...")
    df = load_features().reset_index(drop=True)
    print(f"Loaded {len(df)} base rows, seasons: {sorted(df['game_year'].dropna().unique().tolist())}")

    print("Loading + pivoting sub-model distributional signals...")
    sig = _load_submodel_signals()
    df["game_pk"] = df["game_pk"].astype("int64")
    df = df.merge(sig, on="game_pk", how="left")
    enrich = _enrichment_columns()
    joined = df[enrich[0]].notna().sum()
    print(f"  joined signals onto {joined:,}/{len(df):,} base rows "
          f"({100*joined/max(len(df),1):.1f}%)")

    targets = ["home_win", "run_diff", "total_runs"] if args.target == "all" else [args.target]
    results = {}
    for nm in targets:
        results[nm] = _run_target(nm, df)

    out = _OUT_DIR / ("bayesian_leverage_all.json" if args.target == "all"
                      else f"bayesian_leverage_{args.target}.json")
    out.write_text(json.dumps(results, indent=2))
    print(f"\nWrote {out}")
    print("\n=== DECISIONS (judged on HONEST 2026 only) ===")
    for nm, r in results.items():
        print(f"  {nm:12s} {r['decision']}")


if __name__ == "__main__":
    main()
