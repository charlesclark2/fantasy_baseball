# ── The 52 sub-model distributional columns (layer3_feature_columns.json) ─────
# Env-level (one value per game, side-invariant):
_ENV_COLS = [
    "run_env_mu_v4",
    "run_env_dispersion_v4",
    "run_env_mu_v4_uncertainty",
    "run_env_mu_v4_available",
]
# Per-side base names → pivoted to home_<x> / away_<x>. The base names match the
# raw columns in feature_pregame_sub_model_signals 1:1.
_SIDE_COLS = [
    # offense (pred_runs_v2)
    "pred_runs_mu_v2", "pred_runs_dispersion_v2", "pred_runs_uncertainty_v2", "pred_runs_mu_v2_available",
    # starter suppression (v1)
    "starter_suppression_mu_v1", "starter_suppression_sigma_v1", "starter_uncertainty_v1", "starter_suppression_mu_v1_available",
    # starter innings-pitched (v1)
    "starter_ip_mu_v1", "starter_ip_dispersion_v1", "starter_ip_uncertainty_v1", "starter_ip_mu_v1_available",
    # bullpen (v2)
    "bullpen_mu_v2", "bullpen_dispersion_v2", "bullpen_uncertainty_v2", "bullpen_mu_v2_available",
    # matchup advantage (v1)
    "matchup_advantage_mu_v1", "matchup_advantage_sigma_v1", "matchup_advantage_mu_v1_uncertainty", "matchup_advantage_mu_v1_available",
    # defense quality (v1)
    "defense_quality_mu_v1", "defense_quality_oaa_z_v1", "defense_quality_sprint_z_v1", "defense_quality_mu_v1_available",
]


def _enrichment_columns() -> list[str]:
    cols = list(_ENV_COLS)
    for c in _SIDE_COLS:
        cols.append(f"home_{c}")
        cols.append(f"away_{c}")
    return cols  # 4 + 24*2 = 52


_SIGNAL_QUERY = """
select game_pk, side,
       {raw_cols}
from baseball_data.betting_features.feature_pregame_sub_model_signals
"""


def _load_submodel_signals() -> pd.DataFrame:
    """Pull the long (game_pk x side) signal table and pivot to one wide row per
    game_pk with the 52 enrichment columns. All cols cast to float so the boolean
    *_available indicators survive select_dtypes(number) downstream."""
    raw_cols = ", ".join(_ENV_COLS + _SIDE_COLS)
    conn = get_snowflake_connection()
    try:
        cur = conn.cursor()
        cur.execute(_SIGNAL_QUERY.format(raw_cols=raw_cols))
        cols = [d[0].lower() for d in cur.description]
        rows = cur.fetchall()
    finally:
        conn.close()
    long = pd.DataFrame(rows, columns=cols)
    long["game_pk"] = long["game_pk"].astype("int64")
    long["side"] = long["side"].astype(str).str.lower()

    # env-level: side-invariant → one value per game_pk
    env = long.groupby("game_pk")[_ENV_COLS].max()
    # per-side: pivot home / away
    home = (long[long["side"] == "home"].set_index("game_pk")[_SIDE_COLS].add_prefix("home_"))
    away = (long[long["side"] == "away"].set_index("game_pk")[_SIDE_COLS].add_prefix("away_"))
    wide = env.join(home, how="outer").join(away, how="outer")
    # bool/Decimal → float so imputation + select_dtypes(number) keeps them
    for c in wide.columns:
        wide[c] = pd.to_numeric(wide[c], errors="coerce").astype(float)
    return wide.reset_index()


