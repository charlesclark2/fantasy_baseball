"""Story 30.2 — Bayesian-leverage ABLATION (wire sub-model distributional
outputs into the BASE models).

Controlled ablation: hold each production champion's ARCHITECTURE fixed (the
tuned champion hyperparameters + the 30.4-cleaned contract) and vary ONLY the
feature set —
    champion  = the live 30.4-cleaned contract (home_win 211 / run_diff 169 /
                total_runs 113-seasonnorm)
    enriched  = champion + the 52 sub-model DISTRIBUTIONAL columns (mu /
                dispersion-sigma / uncertainty / available for run_env, offense,
                starter-suppression, starter-IP, bullpen, matchup, defense)
                pivoted home/away from
                baseball_data.betting_features.feature_pregame_sub_model_signals
                — the exact set that today flows ONLY to the Layer-3 stacker
                (betting_ml/models/layer3/layer3_feature_columns.json).

Both arms are scored on:
  1. Walk-forward temporal CV  (all_season_splits, min_train_seasons=3)
  2. The HONEST 2026 out-of-sample surface (train game_year < 2026, eval == 2026)

⚠ LEAKAGE CAVEAT — THE DECISION KEYS ON 2026 ONLY.
  The sub-model signals are generated by generate_*_signals.py scoring ALL 2021+
  games with sub-models FIT on 2021-2025 (memory: project_layer3_signal_leakage).
  So for the 2024 and 2025 CV folds the enriched features are IN-SAMPLE for the
  sub-models → the enriched arm's CV is OPTIMISTICALLY biased and a CV "win" is
  expected/meaningless. 2026 is the only true out-of-sample surface for these
  signals (sub-models trained through 2025). Judge promotion on the 2026 honest
  delta; CV is reported as contaminated context only.

Per the Epic 30 operator directive, the PRIMARY metric is accuracy-to-truth:
  - totals / run_diff : MAE, RMSE, MedAE vs the actual outcome (+ calib_80 for
                        the NGBoost Normal interval).
  - home_win          : Brier, NLL, accuracy, ECE, and live corr of P(home win)
                        vs the 0/1 outcome.
Market comparison is NOT computed here (these are baseball-mechanism signals; the
question is accuracy-to-truth, not edge).

Runtime: RETRAINS NGBoost (run_diff n=1000 / total_runs n=500) and XGBoost
(home_win) per fold for two feature sets — minutes (home_win) to tens of minutes
(NGBoost). Per project convention, hand this off to run with real Snowflake
credentials, ONE --target per invocation.

Usage:
    uv run python betting_ml/scripts/bayesian_leverage_ablation_30_2.py --smoke
    uv run python betting_ml/scripts/bayesian_leverage_ablation_30_2.py --target home_win
    uv run python betting_ml/scripts/bayesian_leverage_ablation_30_2.py --target run_diff
    uv run python betting_ml/scripts/bayesian_leverage_ablation_30_2.py --target total_runs
"""

